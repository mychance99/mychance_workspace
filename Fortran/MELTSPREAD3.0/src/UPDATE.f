      GO TO 5032      
 5031 CONTINUE
      IF(L.GT.2.OR.NBCINT(J).LE.3) GO TO 5033
      GAMCD=(BET(J)*(TFZC(J)-TEMP(2,J)))/ZABLAT(J)  
      GO TO 5032      
 5033 CONTINUE
      CJM12=(FKB(LM1,J)*FKB(L,J))/(FKB(L,J)*XBTW(LM1,J)+FKB(LM1,J)*(XDIS
     1T(LM1,J)-XBTW(LM1,J)))    
      GAMCD=CJM12*(TEMP(LM1,J)-TEMP(L,J)) 
 5032 CONTINUE
