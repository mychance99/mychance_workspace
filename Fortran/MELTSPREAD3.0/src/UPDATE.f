      DXSAT=XDIST(IDR,J)*((TCOMP-TEMP(IDR,J))/(TEMP(IDR+1,J)-TEMP(IDR,J)
     1))    
      IF(DXSAT.GE.XBTW(IDR,J)) GO TO 628  
      IP=IDR
      GO TO 629
  628 CONTINUE
      IP=IDR+1
  629 CONTINUE
