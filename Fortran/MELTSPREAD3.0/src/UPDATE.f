      GO TO 6035      
 6034 CONTINUE
      SDECAK=ROM(21)*QDCU*HBMT(LP1,J,6)+ROM(21)*QDCUO2*HBMT(LP1,J,15)   
 6035 CONTINUE
      EDOTJ=(GAMCD-CJP12*(TEMP(L,J)-TEMP(LP1,J))+SDECAJ)/(DENGJ*VLGJ)   
      EDOTK=(DJM12*(TEMP(L,J)-TEMP(LP1,J))-DJP12*(TEMP(LP1,J)-TEMP(LP2,J
     1))+SDECAK)/(DENGK*VLGJP1) 
