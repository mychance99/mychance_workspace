      IP=IDR
      GO TO 673
  648 CONTINUE
      IP=IDR+1
  673 CONTINUE
      NPROP=NTYPMT(IP,J)
C.....CHECK TO SEE IF ISOTHERM LIES IN GAS PRODUCING REGION.  
      IF(NUMOLD(IOUT,J).GT.1) GO TO 637   
      XLSMF(IOUT,J)=XLEN
      NUMS(IOUT,J)=1  
      IF(NPASSG(J).EQ.1) GO TO 637
      IF(NPROP.GT.3) GO TO 637  
      IF(NL(IP,J).EQ.2) GO TO 637
      I2=IDR+1
      I3=IDR+2
      VL2=XBTW(I2,J)  
      BDCK=ROC*BDCP   
      IF(NTYPMT(I2,J).GT.3) GO TO 1332    
