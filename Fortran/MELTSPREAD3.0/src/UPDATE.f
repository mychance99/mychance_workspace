     1L,J)-XBTW(L,J)))
      DJM12=CJP12     
      DENOM=FKB(LP2,J)*XBTW(LP1,J)+FKB(LP1,J)*(XDIST(LP1,J)-XBTW(LP1,J))
      DJP12=0.D0      
