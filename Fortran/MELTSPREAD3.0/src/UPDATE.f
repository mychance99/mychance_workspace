      IF(L.GT.1) GO TO 5031     
      GAMCD=HTCOEF(J)*(TARGB(J)-TEMP(1,J))
      IF(NBCINT(J).EQ.2) GAMCD=(TKX(J)*(TFZX(J)-TEMP(1,J)))/DCRUST(J)  
