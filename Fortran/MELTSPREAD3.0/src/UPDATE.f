     1EMP(L,J)-TCOMP)*XRFT      
      XDOTG=(XLJJP1*C1)/(TEMP(LP1,J)-TEMP(L,J))**2  
      XDOTG=DMAX1(XDOTG,0.D0)   
      SMFLX(IOUT,J)=BDCK*XDOTG  
      GO TO 652
  637 CONTINUE
  638 CONTINUE
      SMFLX(IOUT,J)=0.D0
  652 CONTINUE
  488 CONTINUE
