     1SIGMLT(999),UMMLT(999),EMIMLT(999)      
      DATA CENT /100.D0/
      DO 3555 KDP=1,NPLTOT
      IUNIT=19+KDP
      KN=NPLLOC(KDP)
      DA=CENT*(ELO(KN)-DABCON(KN))
      DC=CENT*DCRUST(KN)
      DF=CENT*DFILMT(KN)
      EN=1.D-6*ENBLK(KN)
      HM=CENT*HMETAL(KN)
      HO=CENT*HOXIDE(KN)
      HT=CENT*HTOT(KN)
      DW=CENT*DWAT(KN)
      HTV=HT/(1.D0-VOID(KN))
      EL=CENT*ELEVAT(KN)
      HTP=EL+HTV
      HTPC=EL+HT
      HWPC=DW+HTPC
      VG=CENT*VGJ(KN)
      QT=1.D-3*QFLUXT(KN)
      QB=1.D-3*QFLUXB(KN)
      QO=1.D-3*QOXT(KN)
      VL=VEL(KN)*CENT      
      AP=ALPSPR(KN)
C.....WRITE OUT DATA AT THE USER-SELECTED NODE....
      WRITE(IUNIT,3556) KN,TIME,EL,DA,HTPC,HWPC,TBULK(KN),HM,HO,HT,
     1 HTV,HTP,VOID(KN),VG,TEMP(1,KN),VL,QT,QB,QO,DC,DF,
     2 UMMLT(KN),AP
 3556 FORMAT(I5,22(1X,E12.5))     
 3555 CONTINUE
      RETURN
      END